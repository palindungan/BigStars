package com.its.bigstarsapp.Models;

public class BayarSpp {
    String id_bayar_spp, waktu, total_pertemuan, total_harga_spp;
    String id_wali_murid, nama_wali_murid;
    String id_admin;

    public String getId_bayar_spp() {
        return id_bayar_spp;
    }

    public void setId_bayar_spp(String id_bayar_spp) {
        this.id_bayar_spp = id_bayar_spp;
    }

    public String getWaktu() {
        return waktu;
    }

    public void setWaktu(String waktu) {
        this.waktu = waktu;
    }

    public String getTotal_pertemuan() {
        return total_pertemuan;
    }

    public void setTotal_pertemuan(String total_pertemuan) {
        this.total_pertemuan = total_pertemuan;
    }

    public String getTotal_harga_spp() {
        return total_harga_spp;
    }

    public void setTotal_harga_spp(String total_harga_spp) {
        this.total_harga_spp = total_harga_spp;
    }

    public String getId_wali_murid() {
        return id_wali_murid;
    }

    public void setId_wali_murid(String id_wali_murid) {
        this.id_wali_murid = id_wali_murid;
    }

    public String getNama_wali_murid() {
        return nama_wali_murid;
    }

    public void setNama_wali_murid(String nama_wali_murid) {
        this.nama_wali_murid = nama_wali_murid;
    }

    public String getId_admin() {
        return id_admin;
    }

    public void setId_admin(String id_admin) {
        this.id_admin = id_admin;
    }
}
